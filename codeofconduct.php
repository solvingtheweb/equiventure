        <h1> Equiventure Capital Code of Business Conduct </h1><br>

	

<p>This Code of Business Conduct (the “Code”) applies to all directors, officers and 
employees of Equiventure Capital,, LLC and its subsidiaries including, without 
limitation, (the “Company”), who unless otherwise specified, will be referred to jointly as “associates.”  The Code is a statement of goals and expectations for your conduct. 
</p>
<p>
<b>The Code is effective immediately.</b><br /> 
It is important that you read the Code and understand it. Keep it with you and refer to it 
frequently. Ask whatever questions you may have. You are expected to comply with the 
Code in both letter and spirit. Ignorance of the Code will not excuse you from its 
requirements. Together with other guidelines included in the Company’s Associate 
Handbook, and more detailed policies to which you are already subject, the Code will 
help protect our reputation for honesty and integrity.  The Code cannot and is not intended to cover every applicable law or provide answers to all questions that might arise; for that we must ultimately rely on each associate’s good sense of what is right, including a sense of when it is proper to seek guidance from others on the appropriate course of conduct. Because our business depends upon our reputation for honesty and integrity, in many instances, the Code goes beyond the requirements of law. This Code is not an express or implied contract of employment and does not create any contractual rights of any kind between the Company and its associates. In addition, all associates should understand that this Code does not modify their employment relationship, whether at will or governed by contract. 
</p>
<p>
<b>Compliance with Laws</b><br />
Obeying the law, both in letter and spirit, is one of the foundations on which the 
Company’s ethical standards are built. All associates must respect and obey the laws of 
the jurisdictions in which we operate. Although not all associates are expected to know 
the details of these laws, it is important to know enough to determine when to seek advice from supervisors or the General Counsel. 
</p>
<p>
<b>Conflicts of Interest</b><br /> 
Your personal activities and relationships must not conflict or appear to conflict with the 
interest of the Company. Keep in mind, the Code cannot specifically address every 
potential conflict, so use your conscience and common sense. When questions arise seek guidance. 
</p>
<p>
<b>General Principles</b><br /> 
All associates, and all entities in which an associate is an officer or director or has an 
ownership interest, are expected to deal with the Company on an arm’s-length basis. All 
transactions between the Company and any such associates or entities should be approved  in advance by the Audit Committee and, when approved by the Audit Committee, should be promptly disclosed to the entire Board.
</p>
<p>
<b>Accounts and Record Keeping</b><br /> 
Every Company financial record and account must be accurate, timely and in accordance with the law. Our books must reflect all components of transactions, as well as our own standard of insisting upon an honest and forthright presentation. These records are the basis for managing the Company’s business and for fulfilling our obligations to shareholders, associates, tenants and regulatory authorities. 
Accurate records are everyone’s responsibility. It is always a good idea to double-check them. 
</p>
<p>
<b>General Principles</b><br /> 
• Never falsify any document or distort the true nature of any transaction. <br />
• All transactions must be supported by accurate documentation. <br />
• All reports made to regulatory authorities must be full, fair, accurate, timely and 
understandable. <br />
• Associates must cooperate with investigations into the accuracy and timeliness of 
financial records. <br />
• To the extent estimates and accruals are necessary in company reports and records, 
they must be supported by appropriate documentation and based on good faith 
judgment. 
</p>
<p>
<b>Use of Company Assets</b><br /> 
Company assets are meant for Company, not personal, use. Company assets include your time at work and work product, as well as the Company’s equipment, computers and software. Common sense should prevail, of course. The occasional personal phone call to or from your workplace, for example, is inevitable. Substantial personal phone calls, however, represent misuse. The point is to recognize that theft or deliberate misuse of Company assets is a violation of the Code. 
</p>
<p>
<b>General Principles</b><br /> 
• The use of Company assets for your personal benefit or the benefit of anyone other 
than the Company is permitted only with the approval of supervisors having authority 
to permit such usage. <br />
• You may not take away from the Company for yourself any opportunity for financial 
gain that you find out about because of your position at the Company or through the 
use of Company property or information. <br />
• Misuse of the Company assets may be considered theft and result in disciplinary action or criminal prosecution.<br /> 
• Before accepting payment for speeches or presentations related to the Company or 
your work at the Company, always get the approval of the General Counsel or 
another Executive Officer. <br />
• Company computer systems and equipment are meant for Company use only. For 
example, they should never be used for outside businesses, illegal activities, gambling 
or pornography. <br />
</p>
<p>
<b>Working with Outside Parties and Business Gifts</b><br />
Consistent with the obligation we all have to act with integrity and honesty at all times, 
you should deal fairly and in good faith with all persons with whom the Company 
transacts business. No associate should take unfair advantage of anyone through 
misrepresentation or any unfair business practice.  It often is customary to exchange gifts and entertainment with persons with whom we have a business relationship, such as tenants, lenders and investment bankers. The key is to keep an arm’s length relationship. Avoid excessive or lavish gifts that may give the appearance of undue influence. All gifts must be presented in a manne r that clearly identifies the Company and the occasion that warrants the presentation. Avoid personal financial transactions with persons that may influence your ability to perform your job. 
</p>
<p>
<b>General Principles</b><br /> 
• The Code prohibits associates from accepting lavish gifts or entertainment. This is an 
area in which your judgment is critical. For instance, modest holiday gifts are usually 
fine. But an expensive trip probably would not be. In addition, you may not accept 
any gifts in the form of cash. If you are uncertain seek prior approval from the General Counsel and another Executive Officer. <br />
• Gifts and entertainment for persons with whom we have a business relationship mustsupport the legitimate business interests of the Company and should be reasonable 
and appropriate under the circumstance. Protecting the Company’s Nonpublic Information.  It is your obligation to safeguard the Company’s nonpublic information. You should not share confidential information with anyone outside the Company unless it is necessary as part of your work responsibilities.  Nonpublic information is any information that has not been disclosed or made available to the general public. Nonpublic information includes items such as financial data, plans for acquisitions or divestitures, personal information about associates, material contracts, financing transactions, major management changes and other corporate developments. 
</p>
<p>
<b>Administration of the Code - Distribution</b><br /> 
All Company directors, officers and associates will receive a copy of this Code at the 
time they join the Company and will receive any periodic updates. 
</p>
<p>
<b>Approvals</b><br /> 
As described in the Code, certain persons at the Company must review and approve in 
writing any circumstance requiring special permission. Copies of these approva ls should 
be maintained by the Company and made available to auditors or investigators. 
Waivers of any provision of this Code for directors and executive officers must be 
approved by the Board of Directors or its designated committee and promptly will be 
disclosed to the extent required by law or regulation. 
</p>
<p>
<b>Monitoring Compliance</b><br /> 
Associates should take all responsible steps to prevent a Code violation. Associates must report suspected Code violations to the Human Resources executive for the Company or the General Counsel for the Company; provided, however that suspected Code violations which relate to financial statement disclosures or accounting, internal control or auditing matters may be made in accordance with our Procedures for the 
</p>
<p>
<b>Submission of Complaints or Concerns</b><br /> 
Regarding Financial Statement Disclosures, Accounting, Internal Accounting Controls or 
Auditing Matters. If suspected Code violations which relate to financial statement 
disclosures or accounting, internal control or auditing matters are reported to the General Counsel, the General Counsel shall promptly forward such complaints to the chairman of the Audit Committee.  We do not permit retaliation of any kind against associates for good faith reports of potential violations of the Code. Any associate who retaliates against another associate for reporting known or suspected violations of the Code will be in violation of the Code.  Retaliation may also be a violation of law, and as such, could subject both the individual offender and the Company to legal liability. 
</p>
<p>
<b>Investigations and Disciplinary Actions</b> <br />
The Company will investigate any matter so reported and may take appropriate corrective and disciplinary actions, which may include, alone or in combination, a warning or letter of reprimand, demotion, loss of merit increase or bonus, suspension without pay or termination of employment. Violations of this Code are not the only basis for disciplinary action. The Company has additional policies and procedures governing conduct.  
</p>
<p>
<b>Amendments and Modifications</b><br /> 
The Company reserves the rights to amend, alter or terminate this Code at any time and 
for any reason.  
</p>	
	
		

		
