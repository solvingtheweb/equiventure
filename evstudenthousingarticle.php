      FHA/HUD financing. At a time when conventional banks are not lending, and 
      only the strongest developer will make it through the scrutiny of Fannie 
      and Freddie programs, HUD offers another alternative that most student 
      housing developers do not consider.</FONT></P>
      <P><FONT size=2>If a developer is willing to “adapt” his project to meet 
      the guidelines of HUD financing, this can be a very viable option to 
      consider. Many projects that are sitting on the sidelines waiting for the 
      right time, could be restructured and under construction long before the 
      economy changes. Changes to the structure of the leases and sometimes 
      configuration of the rooms will be necessary to adapt to this program. 
      This will at times decrease the profitability of the project but with 
      creative origination and structuring of the loan the prepayment penalties 
      can be reduced to accommodate a refinance within a shorter period of 
      time.</FONT></P>
