      Even some of the biggest developers are being scrutinized by underwriting 
      guidelines that did not exist in prior years due to banks’ wariness of 
      booking any bad loans. The biggest difference in the financing now is that 
      developers have to put more equity into each transaction because loan 
      proceeds are lower. A typical Loan to Cost for a construction loan is 65 – 
      70 percent.</FONT></P>

      <P><FONT size=2>Even though Student Housing has not suffered as much as 
      the other multifamily sectors in this current economic environment, there 
